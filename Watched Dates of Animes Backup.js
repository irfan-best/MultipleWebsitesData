function replaceMonthWithNumber(dateString) {
    return dateString
        .replace(/ January /g, "-01-")
        .replace(/ February /g, "-02-")
        .replace(/ March /g, "-03-")
        .replace(/ April /g, "-04-")
        .replace(/ May /g, "-05-")
        .replace(/ June /g, "-06-")
        .replace(/ July /g, "-07-")
        .replace(/ August /g, "-08-")
        .replace(/ September /g, "-09-")
        .replace(/ October /g, "-10-")
        .replace(/ November /g, "-11-")
        .replace(/ December /g, "-12-");
}


