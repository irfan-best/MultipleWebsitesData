const namesArray = rawNames.split('\n').map(name => name.trim());

// 4. EXECUTE RENAMING
function renameFiles() {
    fs.readdir(folderPath, (err, files) => {
        if (err) {
            console.error("Could not read directory:", err);
            return;
        }

        files.forEach(file => {
            // Regex to find "OP XXXX.jpg" (case insensitive)
            const match = file.match(/OP\s(\d{4})\.(jpg|jpeg|png)/i);

            if (match) {
                const numberStr = match[1]; // e.g. "0001"
                const index = parseInt(numberStr, 10) - 1; // Array is 0-indexed
                const extension = match[2];

                if (namesArray[index]) {
                    const characterName = namesArray[index];
                    const oldPath = path.join(folderPath, file);
                    const newFileName = `OP ${numberStr} - ${characterName}.${extension}`;
                    const newPath = path.join(folderPath, newFileName);

                    fs.rename(oldPath, newPath, (renameErr) => {
                        if (renameErr) {
                            console.error(`Error renaming ${file}:`, renameErr);
                        } else {
                            console.log(`Renamed: ${file} -> ${newFileName}`);
                        }
                    });
                } else {
                    console.warn(`No name found for index ${index + 1} (File: ${file})`);
                }
            }
        });
    });
}

renameFiles();